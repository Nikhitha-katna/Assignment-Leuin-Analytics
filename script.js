function updateAppList(location) {
    const appListContainer = document.getElementById('appList');
    const apps = appsByLocation[location];

    if (!apps) {
      
        appListContainer.innerHTML = alert("No Data Available for Select Location.We Will Add It soon!!");
        appListContainer.style.display = 'block';
        return;
    }

    let appListHTML = `<h2> &nbsp; &nbsp; &nbsp;Mobility Apps Available  In ${location}:</h2><div class="card-container">`;

    for (let category in apps) {
        if (apps.hasOwnProperty(category)) {
            appListHTML += `<p class="main-title">${category}:</p>`; 
            apps[category].forEach(app => {
                appListHTML += `
                    <div class="card">
                        <a href="${app.url}" target="_blank"><img src="${app.image}" alt="${app.name}"></a>
                        <div class="cardbody">
                            <h2>${app.name}</h2>
                            <a href="${app.url}"><button>Book now</button></a>
                        </div>
                    </div>
                `;
            });
        }
    }

    appListHTML += '</div>';
    appListContainer.innerHTML = appListHTML;
    appListContainer.style.display = 'block';
    appListContainer.scrollIntoView({ behavior: 'smooth' });
}


window.onload = function() {
  
    updateAppList('Delhi');
    window.scrollTo({ top: 0, behavior: "instant" }); 
};

